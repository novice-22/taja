/** 낱말 한 판의 단어 목록 (주제별, 인덱스로 순서만 변형) */
export function makeWords(theme: string, index: number): string[] {
  const base = WORD_THEMES[theme] ?? WORD_THEMES[WORD_THEME_NAMES[0]]
  // 인덱스만큼 회전시켜 "다음"을 누르면 순서가 바뀌게
  const shift = index % base.length
  return [...base.slice(shift), ...base.slice(0, shift)]
}

/** 짧은글/긴글 한 판의 문장(줄) 목록 */
export function makeLines(mode: '짧은글' | '긴글', index: number): string[] {
  if (mode === '긴글') {
    const para = LONG[index % LONG.length]
    return para.split(/(?<=[.!?])\s+/).filter(Boolean)
  }
  const N = 5
  return Array.from({ length: N }, (_, i) => SHORT[(index + i) % SHORT.length])
}

/** "다음" 순환 길이 (모드별 콘텐츠 개수) */
export function contentCount(mode: '짧은글' | '긴글'): number {
  return mode === '긴글' ? LONG.length : SHORT.length
}
