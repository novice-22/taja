/** 짧은글 한 판(문장 3개). topic='랜덤'이면 무작위. */
export function makeShort(topic: string, index: number): string[] {
  const N = 3
  if (topic === '랜덤') {
    const all = Object.values(SHORT_THEMES).flat()
    return Array.from({ length: N }, () => all[Math.floor(Math.random() * all.length)])
  }
  const pool = SHORT_THEMES[topic] ?? Object.values(SHORT_THEMES).flat()
  return Array.from({ length: N }, (_, i) => pool[(index + i) % pool.length])
}

