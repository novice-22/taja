/** 한 판에 출제할 단어 수(주제 풀에서 무작위로 뽑음). */
export const WORD_ROUND = 15

/** 낱말 한 판: 주제 풀에서 무작위로 섞어 WORD_ROUND개를 뽑는다. (호출 때마다 랜덤) */
export function makeWords(theme: string, _seed: number): string[] {
  const base = WORD_THEMES[theme] ?? WORD_THEMES[WORD_THEME_NAMES[0]]
  const shuffled = [...base].sort(() => Math.random() - 0.5)
  return shuffled.slice(0, Math.min(WORD_ROUND, shuffled.length))
}

