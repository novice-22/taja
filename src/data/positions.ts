/** 자리연습 카테고리(한컴 타자연습 구성 참고). 사용자가 선택할 수 있게 한다. */
export type PosCategory =
  | '기본자리'
  | '왼손윗자리'
  | '왼손아랫자리'
  | '가운데자리'
  | '오른손윗자리'
  | '오른손아랫자리'
  | '전체자리'
  | '숫자기호'

export const POS_CATEGORIES: PosCategory[] = [
  '기본자리',
  '왼손윗자리',
  '왼손아랫자리',
  '가운데자리',
  '오른손윗자리',
  '오른손아랫자리',
  '전체자리',
  '숫자기호',
]

const CONSONANTS = ['ㄱ', 'ㄴ', 'ㄷ', 'ㄹ', 'ㅁ', 'ㅂ', 'ㅅ', 'ㅇ', 'ㅈ', 'ㅊ', 'ㅋ', 'ㅌ', 'ㅍ', 'ㅎ']
const VOWELS = ['ㅏ', 'ㅑ', 'ㅓ', 'ㅕ', 'ㅗ', 'ㅛ', 'ㅜ', 'ㅠ', 'ㅡ', 'ㅣ', 'ㅐ', 'ㅔ']

// 두벌식 자판의 손/줄 기준 자모 집합
const SETS: Record<PosCategory, string[]> = {
  기본자리: ['ㅁ', 'ㄴ', 'ㅇ', 'ㄹ', 'ㅏ', 'ㅓ', 'ㅗ', 'ㅣ'], // 가장 기초(홈) 키
  왼손윗자리: ['ㅂ', 'ㅈ', 'ㄷ', 'ㄱ', 'ㅅ'], // q w e r t
  왼손아랫자리: ['ㅋ', 'ㅌ', 'ㅊ', 'ㅍ'], // z x c v
  가운데자리: ['ㅁ', 'ㄴ', 'ㅇ', 'ㄹ', 'ㅎ', 'ㅗ', 'ㅓ', 'ㅏ', 'ㅣ'], // a~l 줄
  오른손윗자리: ['ㅛ', 'ㅕ', 'ㅑ', 'ㅐ', 'ㅔ'], // y u i o p
  오른손아랫자리: ['ㅠ', 'ㅜ', 'ㅡ'], // b n m
  전체자리: [...CONSONANTS, ...VOWELS],
  // 숫자 + 자주 쓰는 특수문자(거의 안 쓰는 ~ ` | \ { } [ ] < > ^ 는 제외)
  숫자기호: [
    '1', '2', '3', '4', '5', '6', '7', '8', '9', '0',
    '!', '@', '#', '$', '%', '&', '*', '(', ')',
    '-', '_', '=', '+', ',', '.', '?', '/', ':', ';', "'", '"',
  ],
}

const SEQ_LEN = 24

/**
 * 카테고리의 자모를 섞어 연습 시퀀스를 만든다.
 * Math.random 없이 인덱스 기반으로 변형(*7 step)해 재현 가능.
 */
export function makeJamoSeq(cat: PosCategory, index: number): string[] {
  const s = SETS[cat]
  const out: string[] = []
  for (let i = 0; i < SEQ_LEN; i++) {
    out.push(s[(i * 7 + index * 3) % s.length])
  }
  return out
}
